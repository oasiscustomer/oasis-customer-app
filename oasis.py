# -*- coding: utf-8 -*-
"""
oasis.py

OCR 없이 수동 입력 기반 Streamlit 고객 관리 시스템 (확인 버튼 및 전화번호 포맷 보완)
"""

import streamlit as st
import gspread
from google.oauth2 import service_account
from datetime import datetime
import tempfile
import re

st.title("🚗 세차장 고객 관리 시스템 (수동 입력 버전)")
st.write("📋 차량 번호와 고객 정보를 입력하여 Google Sheets에 기록합니다.")

# ✅ JSON 키 업로드
st.subheader("1️⃣ Google Sheets API 키 파일 업로드")
json_key_path = None
uploaded_key = st.file_uploader("🔑 Google Cloud에서 다운로드한 JSON 키 파일을 업로드하세요.", type=["json"])
if uploaded_key is not None:
    with tempfile.NamedTemporaryFile(delete=False, suffix=".json") as temp_key_file:
        temp_key_file.write(uploaded_key.getbuffer())
        json_key_path = temp_key_file.name
        st.success("✅ 키 파일 업로드 완료")

if not json_key_path:
    st.stop()

# ✅ Google Sheets 인증
try:
    credentials = service_account.Credentials.from_service_account_file(
        json_key_path,
        scopes=["https://www.googleapis.com/auth/spreadsheets", "https://www.googleapis.com/auth/drive"]
    )
    client = gspread.authorize(credentials)
    worksheet = client.open("Oasis Customer Management").worksheet("시트1")
    st.success("✅ Google Sheets 연결 성공!")
except Exception as e:
    st.error(f"🚨 Google Sheets 인증 실패: {e}")
    st.stop()

# ✅ 차량번호 수동 입력
st.subheader("2️⃣ 고객 차량 정보 입력")
plate_input = st.text_input("🔎 차량 번호 (전체 또는 끝 4자리)")
search_button = st.button("🔍 확인")

if search_button and plate_input:
    records = worksheet.get_all_records()
    matched = [r for r in records if plate_input in r['차량번호'] or r['차량번호'].endswith(plate_input)]

    if len(matched) == 1:
        st.success("✅ 기존 고객 확인됨. 방문 기록을 자동으로 업데이트합니다.")
        customer = matched[0]
        row_index = records.index(customer) + 2
        now = datetime.now().strftime("%Y-%m-%d %H:%M")
        date_only = now.split()[0]
        old_log = customer.get("방문기록", "")
        new_log = old_log + f", {now} (1)" if old_log else f"{now} (1)"

        worksheet.update_cell(row_index, 4, date_only)
        worksheet.update_cell(row_index, 5, int(customer.get("총 방문 횟수", 0)) + 1)
        worksheet.update_cell(row_index, 6, new_log)
        st.write("🗓 방문 정보가 성공적으로 업데이트되었습니다.")

    elif len(matched) > 1:
        st.warning("🚨 동일한 끝 4자리를 가진 차량이 여러 대입니다. 전체 차량번호를 선택하세요.")
        options = [r['차량번호'] for r in matched]
        selected = st.selectbox("차량번호 선택", options)
        if selected:
            st.experimental_rerun()

    else:
        st.info("🆕 등록되지 않은 차량입니다. 아래에 고객 정보를 입력해 주세요.")
        full_plate = st.text_input("🚘 전체 차량번호 (예: 160호 7421)")
        raw_phone = st.text_input("📞 고객 전화번호 (예: 01042921289 또는 010-4292-1289)")

        def format_phone(phone):
            digits = re.sub(r'[^0-9]', '', phone)
            if len(digits) == 11:
                return f"{digits[:3]}-{digits[3:7]}-{digits[7:]}"
            return phone

        if st.button("📥 신규 고객 등록") and full_plate and raw_phone:
            phone = format_phone(raw_phone)
            now = datetime.now().strftime("%Y-%m-%d %H:%M")
            date_only = now.split()[0]
            new_row = [full_plate, phone, date_only, date_only, 1, f"{now} (1)"]
            worksheet.append_row(new_row)
            st.success(f"✅ 신규 고객 등록 완료: {full_plate}")