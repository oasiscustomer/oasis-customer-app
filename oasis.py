# -*- coding: utf-8 -*-
"""
oasis.py - 세차장 고객 관리 시스템 (Streamlit WebApp)
- Google Sheets 연동
- 고객 차량 번호 입력 시 중복 확인
- 하루 중복 방문 시 사용자 재확인
"""

import streamlit as st
import gspread
from google.oauth2.service_account import Credentials
from datetime import datetime
import re
import json

# ✅ 인증 처리
credentials = Credentials.from_service_account_info(
    st.secrets["gcp_service_account"],
    scopes=["https://www.googleapis.com/auth/spreadsheets", "https://www.googleapis.com/auth/drive"]
)
client = gspread.authorize(credentials)
worksheet = client.open("Oasis Customer Management").worksheet("시트1")

# ✅ 초기화
st.title("🚗 세차장 고객 관리 시스템")
st.markdown("""
- Google Sheets 연동
- 고객 차량 번호 입력 시 중복 확인
- 하루 중복 방문 시 사용자 재확인
""")

# ✅ 세션 상태 초기화
if "step" not in st.session_state:
    st.session_state.step = "search"
if "matched_customer" not in st.session_state:
    st.session_state.matched_customer = None
if "row_index" not in st.session_state:
    st.session_state.row_index = None

# ✅ 1단계: 고객 차량 번호 입력
st.subheader("2️⃣ 고객 차량 정보 입력")
plate_input = st.text_input("🔎 차량 번호 (전체 또는 끝 4자리)")
if st.button("🔍 확인"):
    records = worksheet.get_all_records()
    matched = [r for r in records if isinstance(r, dict) and (
        ("차량번호" in r and plate_input in str(r["차량번호"])) or
        ("차량번호" in r and str(r["차량번호"]).endswith(plate_input))
    )]

    if len(matched) == 0:
        st.session_state.step = "new"
        st.session_state.matched_customer = None
    elif len(matched) == 1:
        st.session_state.matched_customer = matched[0]
        st.session_state.row_index = records.index(matched[0]) + 2
        st.session_state.step = "check_visit"
    else:
        options = [r["차량번호"] for r in matched if isinstance(r, dict)]
        selected = st.selectbox("🚘 여러 고객이 있습니다. 선택하세요", options)
        customer = next((r for r in matched if r["차량번호"] == selected), None)
        if customer:
            st.session_state.matched_customer = customer
            st.session_state.row_index = records.index(customer) + 2
            st.session_state.step = "check_visit"

# ✅ 기존 고객 방문 여부 확인
if st.session_state.step == "check_visit" and st.session_state.matched_customer:
    customer = st.session_state.matched_customer
    st.success("✅ 기존 고객 확인됨. 방문 기록을 자동으로 업데이트합니다.")
    now = datetime.now().strftime("%Y-%m-%d %H:%M")
    date_only = now.split()[0]
    visit_log = customer.get("방문기록", "")

    if date_only in visit_log:
        st.info("📌 오늘 이미 입력된 기록이 있습니다. 추가로 입력할까요?")
        confirm = st.radio("입력 확인", ["Y", "N"])
        if st.button("✅ 확인"):
            if confirm == "Y":
                new_log = visit_log + f", {now} (1)"
                worksheet.update(f"D{st.session_state.row_index}", [[date_only]])
                worksheet.update(f"E{st.session_state.row_index}", [[int(customer.get("총 방문 횟수", 0)) + 1]])
                worksheet.update(f"F{st.session_state.row_index}", [[new_log]])
                st.success("📅 방문 정보가 성공적으로 추가 입력되었습니다.")
            else:
                st.warning("🚫 추가 입력이 취소되었습니다.")
            st.session_state.step = "done"
    else:
        new_log = visit_log + f", {now} (1)" if visit_log else f"{now} (1)"
        worksheet.update(f"D{st.session_state.row_index}", [[date_only]])
        worksheet.update(f"E{st.session_state.row_index}", [[int(customer.get("총 방문 횟수", 0)) + 1]])
        worksheet.update(f"F{st.session_state.row_index}", [[new_log]])
        st.success("📅 방문 정보가 성공적으로 입력되었습니다.")
        st.session_state.step = "done"

# ✅ 신규 고객 등록
if st.session_state.step == "new":
    st.info("🆕 등록되지 않은 차량입니다. 아래에 고객 정보를 입력해 주세요.")
    full_plate = st.text_input("🚘 전체 차량번호 (예: 160호7421)", key="new_plate")
    raw_phone = st.text_input("📞 고객 전화번호", key="new_phone")

    def format_phone(phone):
        digits = re.sub(r'[^0-9]', '', phone)
        if len(digits) == 11:
            return f"{digits[:3]}-{digits[3:7]}-{digits[7:]}"
        return phone

    if st.button("📥 신규 고객 등록"):
        existing = worksheet.get_all_records()
        exists = any(r.get("차량번호") == full_plate for r in existing)

        if exists:
            st.warning("🚫 이미 등록된 차량번호입니다. 다시 확인해주세요.")
        else:
            now = datetime.now().strftime("%Y-%m-%d %H:%M")
            date_only = now.split()[0]
            phone = format_phone(raw_phone)
            new_row = [full_plate, phone, date_only, date_only, 1, f"{now} (1)"]
            worksheet.append_row(new_row)
            st.success(f"✅ 신규 고객 등록 완료: {full_plate}")
            st.session_state.step = "done"