# oasis.py - 전화번호 하이픈 자동 포맷팅 포함 최종본

import streamlit as st
import gspread
from google.oauth2.service_account import Credentials
from datetime import datetime
import pytz

# 🕒 한국 시간 설정 (KST)
tz = pytz.timezone("Asia/Seoul")
now = datetime.now(tz)
today = now.strftime("%Y-%m-%d")
now_str = now.strftime("%Y-%m-%d %H:%M")

# 🔐 Google 인증 (Sheets + Drive 스코프 포함)
scopes = [
    "https://www.googleapis.com/auth/spreadsheets",
    "https://www.googleapis.com/auth/drive"
]
credentials = Credentials.from_service_account_info(
    st.secrets["gcp_service_account"],
    scopes=scopes
)
client = gspread.authorize(credentials)
worksheet = client.open("Oasis Customer Management").sheet1

# ☎️ 전화번호 포맷 함수
def format_phone_number(phone: str) -> str:
    phone = phone.replace("-", "").strip()
    if len(phone) == 11 and phone.startswith("010"):
        return f"{phone[:3]}-{phone[3:7]}-{phone[7:]}"
    elif len(phone) == 10:
        return f"{phone[:3]}-{phone[3:6]}-{phone[6:]}"
    else:
        return phone

# 🧾 제목
st.markdown("<h1 style='text-align: center; font-size: 28px;'>🚗 세차장 고객 관리 시스템</h1>", unsafe_allow_html=True)
st.subheader("2️⃣ 고객 차량 정보 입력")
plate_input = st.text_input("🔎 차량 번호 (전체 또는 끝 4자리)")
search_button = st.button("🔍 확인")

# 세션 상태 초기화
if "matched_customers" not in st.session_state:
    st.session_state.matched_customers = []
if "selected_plate" not in st.session_state:
    st.session_state.selected_plate = None
if "confirmation_mode" not in st.session_state:
    st.session_state.confirmation_mode = False
if "confirm_update" not in st.session_state:
    st.session_state.confirm_update = None

# 🔍 차량 검색
if search_button and plate_input:
    records = worksheet.get_all_records()
    matched = [
        r for r in records
        if isinstance(r, dict) and (
            plate_input in str(r.get("차량번호", "")) or
            str(r.get("차량번호", "")).endswith(plate_input)
        )
    ]
    st.session_state.matched_customers = matched
    st.session_state.selected_plate = None
    st.session_state.confirmation_mode = False
    st.session_state.confirm_update = None

# 🔁 검색 결과 처리
matched = st.session_state.matched_customers
if matched:
    if len(matched) == 1:
        customer = matched[0]
    else:
        st.warning("🚗 여러 고객이 있습니다. 선택하세요")
        selected_plate = st.selectbox("차량 선택", [r['차량번호'] for r in matched])
        customer = next((r for r in matched if r['차량번호'] == selected_plate), None)
        st.session_state.selected_plate = selected_plate

    if customer:
        visit_log = customer.get("방문기록", "")
        if today in visit_log:
            st.info("📌 오늘 이미 입력된 기록이 있습니다. 추가로 입력할까요?")
            choice = st.radio("입력 확인", ["Y", "N"])
            if st.button("✅ 확인"):
                if choice == "Y":
                    st.session_state.confirm_update = True
                    st.session_state.confirmation_mode = True
                else:
                    st.stop()
        else:
            st.session_state.confirm_update = True
            st.session_state.confirmation_mode = True

        # ✅ 방문 기록 업데이트
        if st.session_state.confirm_update and st.session_state.confirmation_mode:
            all_records = worksheet.get_all_records()
            row_idx = next(
                (i + 2 for i, r in enumerate(all_records) if r['차량번호'] == customer['차량번호']),
                None
            )
            if row_idx:
                prev_count = int(customer.get("총 방문 횟수", 0))
                new_count = prev_count + 1
                old_log = customer.get("방문기록", "")
                new_log = f"{old_log}, {now_str} (1)" if old_log else f"{now_str} (1)"
                try:
                    worksheet.update(f"D{row_idx}", [[today]])
                    worksheet.update(f"E{row_idx}", [[new_count]])
                    worksheet.update(f"F{row_idx}", [[new_log]])
                    st.success("🗓 방문 정보가 성공적으로 업데이트되었습니다.")
                except Exception as e:
                    st.error(f"❌ 업데이트 실패: {e}")
else:
    # 🆕 신규 고객 등록
    st.info("🆕 등록되지 않은 차량입니다. 아래에 고객 정보를 입력해 주세요.")
    new_plate = st.text_input("🚘 전체 차량번호")
    new_phone = st.text_input("📞 고객 전화번호")

    if new_plate and new_phone:
        records = worksheet.get_all_records()
        already_exists_today = any(
            r.get("차량번호") == new_plate and today in str(r.get("방문기록", ""))
            for r in records
        )

        # 전화번호 포맷 적용
        formatted_phone = format_phone_number(new_phone)

        if already_exists_today:
            st.warning("📌 오늘 이미 입력된 고객입니다. 추가 입력할까요?")
            confirm_new = st.radio("입력 확인", ["Y", "N"], key="new_confirm")
            if st.button("✅ 신규 확인"):
                if confirm_new == "Y":
                    try:
                        new_row = [new_plate, formatted_phone, today, today, 1, f"{now_str} (1)"]
                        worksheet.append_row(new_row)
                        st.success("✅ 신규 고객 등록 완료")
                    except Exception as e:
                        st.error(f"❌ 등록 실패: {e}")
                else:
                    st.stop()
        else:
            if st.button("📥 신규 고객 등록"):
                try:
                    new_row = [new_plate, formatted_phone, today, today, 1, f"{now_str} (1)"]
                    worksheet.append_row(new_row)
                    st.success("✅ 신규 고객 등록 완료")
                except Exception as e:
                    st.error(f"❌ 등록 실패: {e}")